# 1. + (Addition) - Сложение двух чисел.
a = 5
b = 3
print(f"1. {a + b = }")  # 8

# 2. - (Subtraction) - Вычитание одного числа из другого.
a = 5
b = 3
print(f"2. {a - b = }")  # 2

# 3. * (Multiplication) - Умножение двух чисел.
a = 5
b = 3
print(f"3. {a * b = }")  # 15

# 4. / (Division) - Деление одного числа на другое.
a = 5
b = 3
print(f"4. {a / b = }")  # 1.6666666666666667

# 5. // (Floor Division) - Деление одного числа на другое с округлением вниз.
a = 5
b = 3
print(f"5. {a // b = }")  # 1

# 6. % (Modulus) - Остаток от деления одного числа на другое.
a = 5
b = 3
print(f"6. {a % b = }")  # 2

# 7. ** (Exponentiation) - Возведение числа в степень.
a = 5
b = 3
print(f"7. {a ** b = }")  # 125

# 8. == (Equal) - Проверка на равенство двух значений.
a = 5
b = 3
print(f"8. {a == b = }")  # False

# 9. != (Not Equal) - Проверка на неравенство двух значений.
a = 5
b = 3
print(f"9. {a != b = }")  # True

# 10. > (Greater Than) - Проверка, больше ли одно значение другого.
a = 5
b = 3
print(f"10. {a > b = }")  # True

# 11. < (Less Than) - Проверка, меньше ли одно значение другого.
a = 5
b = 3
print(f"11. {a < b = }")  # False

# 12. >= (Greater Than or Equal To) - Проверка, больше ли или равно одно значение другому.
a = 5
b = 3
print(f"12. {a >= b = }")  # True

# 13. <= (Less Than or Equal To) - Проверка, меньше ли или равно одно значение другому.
a = 5
b = 3
print(f"13. {a <= b = }")  # False

# 14. and (Logical AND) - Логическое И.
a = True
b = False
print(f"14. {a and b = }")  # False

# 15. or (Logical OR) - Логическое ИЛИ.
a = True
b = False
print(f"15. {a or b = }")  # True

# 16. not (Logical NOT) - Логическое НЕ.
a = True
print(f"16. {not a = }")  # False

# 17. & (Bitwise AND) - Побитовое И.
a = 5  # 0b0101
b = 3  # 0b0011
print(f"17. {a & b = }")  # 1 (0b0001)

# 18. | (Bitwise OR) - Побитовое ИЛИ.
a = 5  # 0b0101
b = 3  # 0b0011
print(f"18. {a | b = }")  # 7 (0b0111)

# 19. ^ (Bitwise XOR) - Побитовое исключающее ИЛИ.
a = 5  # 0b0101
b = 3  # 0b0011
print(f"19. {a ^ b = }")  # 6 (0b0110)

# 20. ~ (Bitwise NOT) - Побитовое НЕ.
a = 5  # 0b0101
print(f"20. {~a = }")  # -6 (0b...11111010)

# 21. << (Bitwise Left Shift) - Побитовый сдвиг влево.
a = 5  # 0b0101
print(f"21. {a << 1 = }")  # 10 (0b1010)

# 22. >> (Bitwise Right Shift) - Побитовый сдвиг вправо.
a = 5  # 0b0101
print(f"22. {a >> 1 = }")  # 2 (0b0010)
